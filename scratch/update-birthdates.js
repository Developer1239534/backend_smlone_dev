async function main() {
  try {
    const lines = rawData.trim().split('\n');
    const records = [];
    const dedupSet = new Set();

    for (const line of lines) {
      const parts = line.split('\t');
      if (parts.length < 1) continue;
      const id = parts[0].trim();
      const bday = parts[1] ? parts[1].trim() : '';
      if (!id || isNaN(id)) continue;
      
      if (dedupSet.has(id)) {
        continue;
      }
      dedupSet.add(id);

      records.push({ id, birthdate: bday || null });
    }

    console.log(`Parsed ${records.length} unique records for birthdate updates.`);

    let updatedCount = 0;
    let missingIds = [];

    for (const record of records) {
      // Check if trainee exists
      const check = await db.query('SELECT trainee_name FROM dashboard_trainne WHERE id = $1', [record.id]);
      if (check.rows.length === 0) {
        missingIds.push(record.id);
        continue;
      }

      await db.query('UPDATE dashboard_trainne SET tanggal_lahir = $1 WHERE id = $2', [record.birthdate, record.id]);
      updatedCount++;
    }

    console.log(`✅ Success! Updated ${updatedCount} birthdates in the database.`);
    if (missingIds.length > 0) {
      console.warn(`⚠️ Warning: The following ${missingIds.length} IDs were not found in the database:`, missingIds);
    }

    process.exit(0);
  } catch (err) {
    console.error('❌ Error during update:', err);
    process.exit(1);
  }
}

main();

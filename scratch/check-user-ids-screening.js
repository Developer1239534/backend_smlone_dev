async function main() {
  try {
    // Read the scan report
    const reportPath = path.join(__dirname, 'screening_test_scan_report.json');
    let titleMap = {};
    if (fs.existsSync(reportPath)) {
      const report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
      // Combine matches and mismatches to find title
      const allItems = [...report.matches, ...report.mismatches, ...report.privateOrSignIn, ...report.errors];
      for (const item of allItems) {
        titleMap[item.id] = item.title || item.error || 'Private/Sign-in';
      }
    }

    // Query DB for userIds
    const placeholders = userIds.map((_, i) => `$${i + 1}`).join(', ');
    const query = `
      SELECT id, trainee_name, screening_test 
      FROM dashboard_trainne 
      WHERE id IN (${placeholders})
      ORDER BY id ASC
    `;
    const res = await db.query(query, userIds);

    const dbMap = {};
    for (const r of res.rows) {
      dbMap[r.id] = r;
    }

    const finalResults = [];
    for (const id of userIds) {
      const dbRow = dbMap[id];
      if (!dbRow) {
        finalResults.push({ id, name: 'NOT FOUND IN DB', url: '-', title: '-' });
        continue;
      }

      const url = dbRow.screening_test || null;
      let title = '-';
      if (url) {
        title = titleMap[id] || 'Laporan / Folder Google Drive';
      }

      finalResults.push({
        id: dbRow.id,
        name: dbRow.trainee_name,
        url: url || '-',
        title: title
      });
    }

    // Write final output as JSON for script utility
    fs.writeFileSync(
      path.join(__dirname, 'user_requested_ids_result.json'),
      JSON.stringify(finalResults, null, 2),
      'utf8'
    );

    console.log(`Successfully checked ${finalResults.length} IDs.`);
    
    // Count how many have links vs don't
    const hasLink = finalResults.filter(r => r.url !== '-').length;
    console.log(`With link: ${hasLink}`);
    console.log(`Without link: ${finalResults.length - hasLink}`);

    process.exit(0);
  } catch (err) {
    console.error(err);
    process.exit(1);
  }
}

main();
